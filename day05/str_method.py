#!/usr/bin/env python3
# -*- coding:utf-8 -*-
py = 'hello world'
py.capitalize() #首字母大写
py.center(20,'#') #剧中
py.rjust(20) #右对齐
py.ljust(20) #左对齐
py.count('l') #统计次数
py.endswith('d') #判断字符串是否以d结尾
py.startswith('h') #判断字符串是否以h开头
py.title() #每个单词首字母大写
py.islower() #转成小写
py.upper() #转成大写
py.isdigit() #判断是否是数字
py.isalpha() #判断是否全是字母
py.isalnum() #所有字符全部是字母或是数字
py.split() #字符切割
py.strip() #去空白
py.lstrip() #去左空白
py.rstrip() #去右空白